<h3>SAVOIR FAIRE</h3>

<b style="background-color:#FF5722">Un pré-détachage minutieux</b>
Tous les vêtements dont nous prenons soin sont d’abord inspectés. Ils sont toujours préparés avant le lavage, c’est ce que nous appelons le pré-détachage. Il est important de noter que l'efficacité du GreenEarth®rend le pré-détachage plus rapide et plus simple. Le vêtement est ainsi examiné, chaque tâche est identifiée puis traitée selon sa nature pour s’assurer qu’elle partira parfaitement au lavage. Pour chaque type de tâche, nous appliquons un produit de pré-détachage ciblé, grâce à une gamme de produits spécialement développée pour notre pressing.<br><br>

<b style="background-color:#FF5722">Un lavage doux et efficace</b>
Nous remplissons notre machine avec du Siloxane, un fluide incolore, inodore et respectueux de l'environnement, qui fait disparaitre les tâches en prenant soin des vêtements. Il assouplit les fibres, adoucit les matières et ravive les couleurs. Les vêtements flottent dans un bain de silicone. Les tâches sont délicatement enlevées. Les vêtements les plus fragiles sont bercés pendant une heure, sans agression, sans mouvements brusques.<br><br>

<b style="background-color:#FF5722">Un repassage plus délicat pour les vêtements</b>
Le procédé de lavage écologique que nous utilisons n’assèche pas les fibres, mais il les protège, les gaine et les renforce, un peu comme un shampoing 2 en 1 pour vos cheveux ! Ainsi, les fibres sont plus soyeuses, plus douces et sont donc plus faciles à repasser. Chez Pressing des Halles, nous repassons ainsi de manière plus délicate, à une température plus basse. Les fibres ne sont pas lustrées et le vêtement reprend naturellement sa forme d’origine.<br><br>
